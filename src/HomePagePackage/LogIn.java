    private void LogInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LogInButtonActionPerformed
        // TODO add your handling code here:
        userid=UserIdField.getText();
        password=PasswordField.getText();
        switch (login_type)
        {
            case "Candidate":
              sql_database_query_get_userid_candidatename();
              break;
            case "Voter":
              sql_database_get_userid_votername_query();
              break;
            case "Admin":
              sql_database_get_userid_adminname_query();
              break;
        }
        if(allowlogin)
        {
            switch (login_type)
            {
            case "Candidate":
              dispose();
              new CandidateHomePage(userid,name,password).setVisible(true);
              break;
            case "Voter":
              dispose();
              new VoterHomePage(userid,name,password).setVisible(true);
              break;
            case "Admin":
              dispose();
              new AdminHomePage(userid,name,password).setVisible(true);
              break;
            }
        }
        else
        {
            LogInResultLabel.setText("Wrong Username and Password.");  
        }
    }//GEN-LAST:event_LogInButtonActionPerformed

