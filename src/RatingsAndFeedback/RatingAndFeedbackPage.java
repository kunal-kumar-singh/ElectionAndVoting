    public void sql_RatingsAndFeedback_insert_query()
    {
       
        try{
           Class.forName("com.mysql.jdbc.Driver");
           String url="jdbc:mysql://localhost:3306/ElectionAndVoting";
           Connection connection=DriverManager.getConnection(url,"root","");
           String query="INSERT INTO RatingsAndFeedback VALUES (?, ?, ?, ?, ?, ?)";
           PreparedStatement prestat;
           prestat = connection.prepareStatement(query);
           prestat.setString(1, electionid);
           prestat.setString(2, candidateid);
           prestat.setString(3, constituency);
           prestat.setInt(4, rating);
           prestat.setString(5, feedback);
           prestat.setString(6,"False");
           prestat.executeUpdate();
           connection.close();
        }
        catch(Exception exception)
        { 
            System.out.println(exception);
        } 
        
    }
    
    void sql_RatingsAndFeedback_fetch_query()
    {
       
        try{
           Class.forName("com.mysql.jdbc.Driver");
           String url="jdbc:mysql://localhost:3306/ElectionAndVoting";
           Connection connection=DriverManager.getConnection(url,"root","");
           String query="SELECT Feedback,Ratings FROM RatingsAndFeedback WHERE UserId = "+"'"+candidateid+"'"+" AND Constituency = "+"'"+constituency+"'"+" AND ElectionId = "+"'"+electionid+"'"+"AND Retrived = 'False' ;";
           PreparedStatement prestat;
           prestat = connection.prepareStatement(query);
           ResultSet results;
           results=prestat.executeQuery();
           while(results.next()){
           feedback=results.getString("Feedback");
           rating=results.getInt("Ratings");
           ratingstring=Integer.toString(rating);                               
           }
           connection.close();
        }
        catch(Exception exception)
        { 
            System.out.println(exception);
        } 
        
    }
    
