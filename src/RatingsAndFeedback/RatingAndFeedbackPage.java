    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
        // TODO add your handling code here:
        feedback=FeedbackField.getText();
        ratingstring=RatingField.getText();
        rating=Integer.parseInt(ratingstring);
        if(rating<1||rating>5)
        ExecutionResult.setText("Please Enter Valid Rating!");
        else
        {
            sql_RatingsAndFeedback_insert_query();
            ExecutionResult.setText("Rating and Feedback Successfully Submitted");
            dispose();
            new VoterHomePage(user_id,voter_name,password).setVisible(true);
        }
    }//GEN-LAST:event_SubmitButtonActionPerformed

